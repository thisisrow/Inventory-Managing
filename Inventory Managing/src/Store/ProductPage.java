    public void connect()
    {
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/luffystore", "root", "root");
            stmt = con.createStatement();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }
    public void showData()
    {
        try {
            String sql="select *from product";
            rs=stmt.executeQuery(sql);
            
            DefaultTableModel model=(DefaultTableModel)jTable1.getModel();
            model.setRowCount(0);
            while(rs.next())
            {
                model.addRow(new Object[]{rs.getInt("id"),rs.getString("name"),rs.getString("price"),rs.getString("quantity"),rs.getString("category"),rs.getString("supplier")});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    public void addCategory()
    {
        try {
            String sql = "Select * from category";
            
            rs = stmt.executeQuery(sql);
            while(rs.next())
            {
                cat.addItem(rs.getString("catname"));
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }
   public void addSupplier()
    {
        try {
            String sql = "Select * from supplier";
            
            rs = stmt.executeQuery(sql);
            while(rs.next())
            {
                supp.addItem(rs.getString("name"));
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }

