    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        // TODO add your handling code here:
        if(searchOption.getSelectedItem().equals("Select"))
        {
            JOptionPane.showMessageDialog(this, "Please select Option in option list ");
        }
        else if(searchField.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this, "Please Enter data from textfiled ");
        }
        else{
            String sql = "select * from product where "+searchOption.getSelectedItem()+" like '%"+searchField.getText()+"%' ";
            try {
                rs = stmt.executeQuery(sql);
                DefaultTableModel model=(DefaultTableModel) jTable1.getModel();
                model.setRowCount(0);
                while(rs.next())
                {
                    model.addRow(new Object[]{rs.getString("id"),rs.getString("name"),rs.getString("price"),rs.getString("quantity"),rs.getString("category"),rs.getString("supplier")});
                }
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_searchActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        DefaultTableModel model=(DefaultTableModel)jTable1.getModel();
        id=Integer.parseInt(model.getValueAt(jTable1.getSelectedRow(), 0).toString());
       
        name.setText(model.getValueAt(jTable1.getSelectedRowCount(), 1).toString());
        price.setText(model.getValueAt(jTable1.getSelectedRowCount(), 2).toString());
        quantity.setText(model.getValueAt(jTable1.getSelectedRowCount(), 3).toString());
        cat.setSelectedItem(model.getValueAt(jTable1.getSelectedRow(), 4).toString());
        supp.setSelectedItem(model.getValueAt(jTable1.getSelectedRow(), 5).toString());
    }//GEN-LAST:event_jTable1MouseClicked

